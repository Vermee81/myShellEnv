local opt = vim.opt

-- 行番号の設定
opt.number = true
opt.relativenumber = true

-- タブとインデントの設定
opt.tabstop = 4
opt.shiftwidth = 4
opt.expandtab = true

-- 検索設定
opt.ignorecase = true
opt.smartcase = true
opt.hlsearch = true
opt.incsearch = true

-- 折り返し設定
opt.wrap = true
opt.linebreak = true

-- ビジュアル設定
opt.cursorline = true


